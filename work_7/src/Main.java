public class Main {
    public static void main(String[] args) {
        // Пример для проверки: зададим значение переменной a
        int a = 2;

        // Исходный код инициализации переменной i
        int i = 0;

        // Компактная запись всей конструкции if-else через тернарный оператор
        i = (a == 1) ? 1 : ((a == 2) ? 2 : 3);

        System.out.println("При a = " + a + " значение i равно: " + i); // Выведет 2
    }
}
