// СТРУКТУРА КЛАССОВ ДЛЯ ПРАКТИКИ #1 И #2
// ==========================================

class A {
    // Поле для демонстрации Практики #2 и Варианта 2 (Практика 1)
    int a = 100;

    // Конструктор для Варианта 1 (Практика 1)
    A() {
        System.out.println("Вариант 1: Вызван конструктор суперкласса через super()");
    }

    // Метод для демонстрации Практики #2 и Варианта 3 (Практика 1)
    void method() {
        System.out.println("Вызван method() из базового класса A");
    }
}

// Класс B пустой, как описано в условии Практики #2
class B extends A {
    // Конструктор по умолчанию автоматически вызывает super() скрыто от глаз
}

class C extends B {
    // Переменная для демонстрации сокрытия полей в Практике 1
    int a = 5;

    C() {
        // Вариант 1 использования super: Вызов конструктора родительского класса
        super();
    }

    @Override
    void method() {
        System.out.println("Вызван переопределенный method() в классе C");
    }

    void demonstrateSuper() {
        System.out.println("\n--- Демонстрация работы ключевого слова super ---");

        // Вариант 2 использования super: Доступ к полю суперкласса (даже если оно скрыто в текущем)
        System.out.println("Значение локального поля 'this.a': " + this.a);
        System.out.println("Вариант 2: Значение унаследованного 'super.a' (взято из класса A через B): " + super.a);

        // Вариант 3 использования super: Вызов метода суперкласса (игнорируя переопределение в C)
        System.out.print("Вариант 3: ");
        super.method();
    }
}

// ==========================================
// ГЛАВНЫЙ КЛАСС ДЛЯ ЗАПУСКА
// ==========================================
public class Main {
    public static void main(String[] args) {
        // Создание объекта автоматически демонстрирует Вариант 1 (вызов конструктора)
        C objectC = new C();

        // Демонстрация Вариантов 2 и 3, а также подтверждение логики Практики #2
        objectC.demonstrateSuper();
    }
}
