class D {
    int a;
    int b;
    int c;
    int z;

    // Конструктор без параметров: вызывает конструктор с одним параметром, передавая 0
    public D() {
        this(0);
    }

    // Конструктор с одним параметром: вызывает конструктор с двумя параметрами
    public D(int a) {
        this(a, 0);
    }

    // Конструктор с двумя параметрами: вызывает самый полный конструктор с тремя параметрами
    public D(int a, int b) {
        this(a, b, 0);
    }

    // Самый полный конструктор: выполняет реальную инициализацию всех полей
    public D(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.z = 1; // Общая операция для всех конструкторов теперь выполняется один раз здесь
    }

    // Вывод состояния объекта для проверки работоспособности
    void display() {
        System.out.println("Объект класса A -> a: " + a + ", b: " + b + ", c: " + c + ", z: " + z);
    }
}

// ==========================================
// ГЛАВНЫЙ КЛАСС ДЛЯ ЗАПУСКА
// ==========================================
public class Main_2 {
    public static void main(String[] args) {
        System.out.println("--- Создание объектов через цепочку конструкторов ---");

        D obj1 = new D();
        obj1.display(); // Все поля 0, z = 1

        D obj2 = new D(10);
        obj2.display(); // a = 10, остальные 0, z = 1

        D obj3 = new D(10, 20);
        obj3.display(); // a = 10, b = 20, c = 0, z = 1

        D obj4 = new D(10, 20, 30);
        obj4.display(); // a = 10, b = 20, c = 30, z = 1
    }
}
