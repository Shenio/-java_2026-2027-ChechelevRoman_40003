import java.util.Date;

public class Main {
    public static void main(String[] args) {

        // ==========================================
        // ПРАКТИКА #1: 5 спецификаторов общего формата
        // ==========================================
        System.out.println("=== Практика #1: Общее форматирование ===");

        // Используем %d (целое), %f (дробное), %s (строка), %b (логическое), %c (символ)
        // %n используется для кроссплатформенного переноса строки
        System.out.printf("Строка: %s%n", "Привет, Java");
        System.out.printf("Целое число: %d%n", 42);
        System.out.printf("Число с плавающей точкой (округлено): %.2f%n", 3.14159);
        System.out.printf("Символ: %c%n", 'J');
        System.out.printf("Логическое значение: %b%n", true);
        System.out.println();


        // ==========================================
        // ПРАКТИКА #2: Демонстрация flush()
        // ==========================================
        System.out.println("=== Практика #2: Демонстрация flush() ===");

        System.out.print("Данные уходят в поток...");
        System.out.flush();
        System.out.println(" Готово!\n");


        // ==========================================
        // ПРАКТИКА #3: 5 спецификаторов формата времени и даты
        // ==========================================
        System.out.println("=== Практика #3: Форматирование даты и времени ===");

        Date now = new Date();

        // Используем %tH (час), %tM (минуты), %tY (4-значный год), %tB (месяц словом), %tA (день недели)
        System.out.printf("Текущий час (00-23): %tH%n", now);
        System.out.printf("Текущие минуты: %tM%n", now);
        System.out.printf("Год в 4-значном формате: %tY%n", now);
        System.out.printf("Полное название месяца: %tB%n", now);
        System.out.printf("Полное название дня недели: %tA%n", now);
    }
}
